class Patient {
  final String id;
  final String patientName;
  final String email;
  final String cnic;
  final String age;
  final String phone;
  final String gender;


  Patient({
    required this.id,
    required this.patientName,
    required this.email,
    required this.cnic,
    required this.age,
    required this.gender,
    required this.phone,
  });
}
